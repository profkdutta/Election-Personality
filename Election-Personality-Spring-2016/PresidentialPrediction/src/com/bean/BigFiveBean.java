package com.bean;

public class BigFiveBean {
	
	int id;
	
	/**
	 * to store conscientiousness value
	 */
	int conscientiousnessBean;
	
	/**
	 * to store openness value
	 */
	int opennessBean;
	
	/**
	 * to store agreeableness value
	 */
	int agreeablenessBean;
	
	/**
	 * to store emotionalrange value
	 */
	int emotionalRangeBean;
	
	/**
	 * to store introversio/extraversion value
	 */
	int introversionBean;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the conscientiousnessBean
	 */
	public int getConscientiousnessBean() {
		return conscientiousnessBean;
	}

	/**
	 * @param conscientiousnessBean the conscientiousnessBean to set
	 */
	public void setConscientiousnessBean(int conscientiousnessBean) {
		this.conscientiousnessBean = conscientiousnessBean;
	}

	/**
	 * @return the opennessBean
	 */
	public int getOpennessBean() {
		return opennessBean;
	}

	/**
	 * @param opennessBean the opennessBean to set
	 */
	public void setOpennessBean(int opennessBean) {
		this.opennessBean = opennessBean;
	}

	/**
	 * @return the agreeablenessBean
	 */
	public int getAgreeablenessBean() {
		return agreeablenessBean;
	}

	/**
	 * @param agreeablenessBean the agreeablenessBean to set
	 */
	public void setAgreeablenessBean(int agreeablenessBean) {
		this.agreeablenessBean = agreeablenessBean;
	}

	/**
	 * @return the emotionalRangeBean
	 */
	public int getEmotionalRangeBean() {
		return emotionalRangeBean;
	}

	/**
	 * @param emotionalRangeBean the emotionalRangeBean to set
	 */
	public void setEmotionalRangeBean(int emotionalRangeBean) {
		this.emotionalRangeBean = emotionalRangeBean;
	}

	/**
	 * @return the introversionBean
	 */
	public int getIntroversionBean() {
		return introversionBean;
	}

	/**
	 * @param introversionBean the introversionBean to set
	 */
	public void setIntroversionBean(int introversionBean) {
		this.introversionBean = introversionBean;
	}
}
